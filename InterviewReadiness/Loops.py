#1. For loop: This is used when we know until what loop count, the iteration is expected
print(f'This is Problem-3.1 on for loop')
for i in range (3):
    print(i)



#2. WhileFor loop: This is used when we do not know until what loop count, the iteration is expected for a given condition when met

print(f'\nThis is Problem-3.2 on while loop')
ctr = 10
while  ctr < 20:
    print(f'At {ctr}, the value is less')
    ctr += 1
